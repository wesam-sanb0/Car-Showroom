    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            String idStr = jTextField1.getText().trim();
            String make = jTextField2.getText().trim();
            String model = jTextField3.getText().trim();
            String yearStr = jTextField4.getText().trim();
            String priceStr = jTextField5.getText().trim();
            String type = jTextField6.getText().trim();
            String feature = jTextField7.getText().trim();
            String imagePath = jTextField8.getText().trim();

            if (idStr.isEmpty() || make.isEmpty() || model.isEmpty() || yearStr.isEmpty() || priceStr.isEmpty() || type.isEmpty() || imagePath.isEmpty()) {
                throw new IllegalArgumentException("Please fill all required fields.");
            }

            int carID = Integer.parseInt(idStr);
            if (carID <= 0) {
                throw new IllegalArgumentException("Car ID must be positive.");
            }

            if (!make.matches("[a-zA-Z\\s]+")) {
                throw new IllegalArgumentException("Make should contain only letters.");
            }
            if (!model.matches("[a-zA-Z0-9\\s]+")) {
                throw new IllegalArgumentException("Model should not contain special characters.");
            }
            if (!type.matches("[a-zA-Z\\s]+")) {
                throw new IllegalArgumentException("Type should contain only letters.");
            }

            int year = Integer.parseInt(yearStr);
            int currentYear = java.time.Year.now().getValue();
            if (year < 1900 || year > currentYear) {
                throw new IllegalArgumentException("Year must be between 1900 and " + currentYear + ".");
            }

            double price = Double.parseDouble(priceStr);
            if (price <= 0) {
                throw new IllegalArgumentException("Price must be greater than 0.");
            }

            File originalFile = new File(imagePath);
            String imageFileName = originalFile.getName();
            File destinationDir = new File("src/images");
            if (!destinationDir.exists()) destinationDir.mkdirs();
            File destinationFile = new File(destinationDir, imageFileName);
            Files.copy(originalFile.toPath(), destinationFile.toPath(), StandardCopyOption.REPLACE_EXISTING);

            String storedPath = "images/" + imageFileName;

            personalQuery pq = new personalQuery();
            boolean success = pq.insertCar(carID, make, model, year, price, type, feature, storedPath);

            if (success) {
                dispose();
                new ViewCarsPage(employeeID).setVisible(true);
            }

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Car ID, Year, and Price must be valid numbers.", "Input Error", JOptionPane.ERROR_MESSAGE);
        } catch (IllegalArgumentException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Validation Error", JOptionPane.ERROR_MESSAGE);
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Failed to save image file.", "File Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

