    private void loadCarDetails() {
        try (Connection conn = new personalQuery().getConnection()) {
            String sql = "SELECT * FROM Car WHERE CarID = ?";
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setInt(1, carID);
            ResultSet rs = stmt.executeQuery();
            if (rs.next()) {
                jLabel4.setText(rs.getString("Make"));
                jLabel5.setText(rs.getString("Model"));
                jLabel6.setText(String.valueOf(rs.getInt("CarYear")));
                carPrice = rs.getDouble("Price");
                jLabel7.setText("$" + carPrice);
                jLabel8.setText(rs.getString("Type"));
                jLabel9.setText(rs.getString("Features"));

String imagePath = rs.getString("ImagePath");
if (imagePath != null && !imagePath.isEmpty()) {
    try {
        File imageFile = new File("src/" + imagePath);
        if (imageFile.exists()) {
            ImageIcon icon = new ImageIcon(imageFile.getAbsolutePath());
            Image img = icon.getImage().getScaledInstance(150, 120, Image.SCALE_SMOOTH);
            jLabel1.setIcon(new ImageIcon(img));
        } else {
            jLabel1.setIcon(null);
        }
    } catch (Exception e) {
        jLabel1.setIcon(null);
    }
}

            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
        }
    }

    private void placeOrder() {
        int confirm = JOptionPane.showConfirmDialog(this, "Confirm placing the order?", "Confirm", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            try (Connection conn = new personalQuery().getConnection()) {
                String sql = "INSERT INTO OrderTable (CarID, CustomerID, TotalPrice, Status, OrderDate) VALUES (?, ?, ?, 'Approved', ?)";
                PreparedStatement stmt = conn.prepareStatement(sql, Statement.RETURN_GENERATED_KEYS);
                stmt.setInt(1, carID);
                stmt.setInt(2, customerID);
                stmt.setDouble(3, carPrice);
                stmt.setDate(4, java.sql.Date.valueOf(LocalDate.now()));

                int rows = stmt.executeUpdate();
                if (rows > 0) {
                    JOptionPane.showMessageDialog(this, "Order placed successfully!");
                    dispose();
                    new CustomerPlatForm(customerID).setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to place order!");
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
            }
        }
    }

    private void requestTestDrive() {
        try {
            String dateInput = jTextField1.getText().trim();
            String timeInput = jTextField2.getText().trim();

            if (dateInput.isEmpty() || timeInput.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter date and time.");
                return;
            }

            LocalDate date = LocalDate.parse(dateInput);
            LocalTime time = LocalTime.parse(timeInput);

            if (date.isBefore(LocalDate.now())) {
                JOptionPane.showMessageDialog(this, "Date must be today or later!");
                return;
            }

            int confirm = JOptionPane.showConfirmDialog(this, "Confirm test drive?", "Confirm", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                try (Connection conn = new personalQuery().getConnection()) {
String sql = "INSERT INTO TestDrive (CustomerID, CarID, EmployeeID, Date, Time) VALUES (?, ?, ?, ?, ?)";
PreparedStatement stmt = conn.prepareStatement(sql);
stmt.setInt(1, customerID);
stmt.setInt(2, carID);
stmt.setNull(3, java.sql.Types.INTEGER);
stmt.setDate(4, Date.valueOf(date));
stmt.setTime(5, Time.valueOf(time));

                    int rows = stmt.executeUpdate();
                    if (rows > 0) {
                        JOptionPane.showMessageDialog(this, "Test drive requested successfully!");
                        dispose();
                        new CustomerPlatForm(customerID).setVisible(true);
                    } else {
                        JOptionPane.showMessageDialog(this, "Failed to request test drive!");
                    }
                }
            }
        } catch (DateTimeParseException e) {
            JOptionPane.showMessageDialog(this, "Invalid date or time format!");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
        }
    }
    
