    private void deleteCarFromDatabase(int carID) {
    try (Connection conn = new personalQuery().getConnection()) {
        conn.setAutoCommit(false); 

        PreparedStatement deleteOrders = conn.prepareStatement("DELETE FROM OrderTable WHERE CarID = ?");
        deleteOrders.setInt(1, carID);
        deleteOrders.executeUpdate();

        PreparedStatement deleteTestDrives = conn.prepareStatement("DELETE FROM TestDrive WHERE CarID = ?");
        deleteTestDrives.setInt(1, carID);
        deleteTestDrives.executeUpdate();

        PreparedStatement deleteCar = conn.prepareStatement("DELETE FROM Car WHERE CarID = ?");
        deleteCar.setInt(1, carID);
        int rowsDeleted = deleteCar.executeUpdate();

        if (rowsDeleted > 0) {
            conn.commit(); 
            JOptionPane.showMessageDialog(this, "Car deleted successfully!");
            dispose();
            new ViewCarsPage(employeeID).setVisible(true);
        } else {
            conn.rollback();
            JOptionPane.showMessageDialog(this, "Car not found.");
        }

    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Database Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }
    
