private void loadCarDetailsFromDatabase() {
        try (Connection conn = new personalQuery().getConnection()) {
            String sql = "SELECT * FROM Car WHERE CarID = ?";
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setInt(1, carID);
            ResultSet rs = stmt.executeQuery();
            if (rs.next()) {
                label3.setText(rs.getString("Make"));
                label5.setText(rs.getString("Model"));
                label7.setText(String.valueOf(rs.getInt("CarYear")));
                double CarPrice = rs.getDouble("Price");
                label9.setText("$" + CarPrice);
                label12.setText(rs.getString("Type"));
                label13.setText(rs.getString("Features"));

String imagePath = rs.getString("ImagePath");
if (imagePath != null && !imagePath.isEmpty()) {
    try {
        File imageFile = new File("src/" + imagePath);
        if (imageFile.exists()) {
            ImageIcon icon = new ImageIcon(imageFile.getAbsolutePath());
            Image img = icon.getImage().getScaledInstance(150, 120, Image.SCALE_SMOOTH);
            jLabel1.setIcon(new ImageIcon(img));
        } else {
            jLabel1.setIcon(null);
        }
    } catch (Exception e) {
        jLabel1.setIcon(null);
    }
}

            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
        }
}

