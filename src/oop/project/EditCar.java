    private void loadCarData() {
        try {
            String sql = "SELECT * FROM Car WHERE CarID = ?";
            PreparedStatement stmt = connection.prepareStatement(sql);
            stmt.setInt(1, carID);
            ResultSet rs = stmt.executeQuery();

            if (rs.next()) {
                makeField.setText(rs.getString("Make"));
                modelField.setText(rs.getString("Model"));
                yearField.setText(String.valueOf(rs.getInt("CarYear")));
                double carPrice = rs.getDouble("Price");
                priceField.setText(String.valueOf(rs.getDouble("Price")));
                typeField.setText(rs.getString("Type"));
                featuresArea.setText(rs.getString("Features"));
                selectedImagePath = rs.getString("ImagePath"); // if exists
                
            } else {
                JOptionPane.showMessageDialog(this, "Car not found!");
                openCarDetailsPage();
            }

            rs.close();
            stmt.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading car data!");
            openCarDetailsPage();
        }
    }

private void saveCarChanges() {
    try {
        String make = makeField.getText().trim();
        String model = modelField.getText().trim();
        String type = typeField.getText().trim();
        String features = featuresArea.getText().trim();

        if (make.isEmpty() || model.isEmpty() || type.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields!");
            return;
        }

        int year = Integer.parseInt(yearField.getText().trim());
        double price = Double.parseDouble(priceField.getText().trim());

        Connection conn = new personalQuery().getConnection();
        String sql = "UPDATE Car SET Make = ?, Model = ?, CarYear = ?, Price = ?, Type = ?, Features = ?, ImagePath = ? WHERE CarID = ?";
        PreparedStatement stmt = conn.prepareStatement(sql);

        stmt.setString(1, make);
        stmt.setString(2, model);
        stmt.setInt(3, year);
        stmt.setDouble(4, price);
        stmt.setString(5, type);
        stmt.setString(6, features);
        stmt.setString(7, selectedImagePath);
        stmt.setInt(8, carID);

        int rowsAffected = stmt.executeUpdate();
        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(this, "Car updated successfully!");
            openCarDetailsPage();
        } else {
            JOptionPane.showMessageDialog(this, "No changes were made or car not found.");
        }

        stmt.close();
        conn.close();
    } catch (NumberFormatException nfe) {
        JOptionPane.showMessageDialog(this, "Please enter valid numbers for Year and Price!", "Invalid Input", JOptionPane.ERROR_MESSAGE);
    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Database error while saving changes!", "Database Error", JOptionPane.ERROR_MESSAGE);
    }
}


