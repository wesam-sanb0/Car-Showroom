    private void chooseImageButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chooseImageButtonActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        int result = fileChooser.showOpenDialog(null);

        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            try {
                String fileName = selectedFile.getName();
                File destDir = new File("src/images");
                if (!destDir.exists()) destDir.mkdirs();
                File destFile = new File(destDir, fileName);
                Files.copy(selectedFile.toPath(), destFile.toPath(), StandardCopyOption.REPLACE_EXISTING);
                selectedImagePath = "images/" + fileName; // relative path
                JOptionPane.showMessageDialog(this, "Image selected and copied.");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Failed to copy image.");
            }
        }
    }//GEN-LAST:event_chooseImageButtonActionPerformed

